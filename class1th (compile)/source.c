#include<stdio.h>
// 전처리기 : 프로그래이 컴파일되기 이전에 프로그램에 대한 사전 처리를 하는 과정입니다.

// main() : 프로그램의 진입점 역할을 수행하는 함수
void main()
{
	// ;(세미 콜론) : 한 문장의 끝을 알려주는 기호입니다.
	printf("사막\n");
	
	// \n (개행 문자) : 해당하는 위치를 한 간 아래로 내려주는 문자
	printf("신짜오");

	// 전처리기는 컴파일러가 아닌 선행처리기에 의해서 처리되는 문자이기 때문에
	// 명령문 끝에 ";"을 상용하지 않습니다.
}